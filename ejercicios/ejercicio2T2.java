public class ejercicio2T2 {

    public static void main(String[] args) {
        // Mayor número entero representable en char
        char maxChar = Character.MAX_VALUE;
        System.out.println("Mayor número entero en char: " + (int) maxChar);

        // Mayor número entero representable en short
        short maxShort = Short.MAX_VALUE;
        System.out.println("Mayor número entero en short: " + maxShort);

        // Mayor número entero representable en int
        int maxInt = Integer.MAX_VALUE;
        System.out.println("Mayor número entero en int: " + maxInt);
    }
}
